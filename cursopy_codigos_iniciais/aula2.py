# Imprimindo: print('')
# Comando utilizado para exibir comandos/textos em tela, 
# informando os argumentos não nomeados
print(12, 34)
print(24, 64)

# Utilizando separadores: sep='' ou sep=""
# Comando utilizado para alterar o estilo de separação utilizado
# Argumento nomeado
# Exemplo abaixo:

print(12, 34, sep="^")
print(12, 34, sep='-')

# Outro Argumento Nomeado: end='' ou end=""
# Comando utilizado para alterar o estilo de separação utilizado
# \n = pula linha  \r = 
# Exemplo abaixo:

print(12, 34, sep="^", end='\n')
print(12, 34, sep='-', end='##')