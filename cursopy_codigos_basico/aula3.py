# Tipos de Dados:
# Python é uma linguagem de programação de Tipo de Tiagem = Dinamica / Forte
# str > String > Text
# Strings são Textos utilizados em dentros de Aspas, simples ou Duplas
# Exemplo


# Aspas Simples
print('Texto')
print(1, 'Texto')

#Aspas Duplas
print("Texto")

# Escape
print('Texto \"texto\" texto')

# Escape melhorado:
print('Texto "texto" texto')
print("Texto 'texto' texto")