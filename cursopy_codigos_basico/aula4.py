# Tipos int e float:
# Int > Número inteiro
# o tipo int representa qualquer numero positivo ou negativo

print(11)  #int
print(-11) #int 
print(0)   #int 

# Float > Número com ponto flutuante
# O tipo Float representa qualquer número positivo ou negativo
# com ponto flutuante.
# o . é utulizado para separar as casas decimais

print(1.1) #Float 
print(1.33) #Float 
print(-1.32) #Float 

# A função type mostra o tipo que o Python inferiu ao valor
print(type('Lucas'))
print(type(1))
print(type(-1.1))
print(type(-1.1),type(2))