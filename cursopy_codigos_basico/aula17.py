# Operações condicionais:
# if /    elif   /  else
# se / se não se / se não

condicao1 = False
condicao2 = False
condicao3 = False
condicao4 = False

print('Fora do bloco 1')

if condicao1 == True :
    print('Condicao 1 é a correta')
elif condicao2 == True :
    print('Condicao 2 é a correta')
elif condicao3 == True :
    print('Condicao 3 é a correta')
elif condicao4 == True :
    print('Condicao 4 é a correta')
else :
    print('Nenhuma condicação é a correta')

print('Fora do bloco 2')